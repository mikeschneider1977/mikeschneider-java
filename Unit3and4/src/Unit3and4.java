public class Unit3and4 {
    public static void main(String[] args){

        // hello tacoma on one line using print
        System.out.print("Hello ");
        System.out.print("Tacoma");

        System.out.println();
        System.out.println();

        // hello tacoma on two lines using println
        System.out.println("Hello ");
        System.out.println("Tacoma");

        System.out.println();
        System.out.println();

        /* hello tacoma on two lines using
         the line feed escape sequence */
        System.out.println("Hello \nTacoma");

        System.out.println();
        System.out.println();

        /* hello tacoma in quotes using
         the quote escape sequence */
        System.out.println("\"Hello Tacoma\"");

        System.out.println();
        System.out.println();

        /* hello tacoma with tab space
        using the tab escape sequence */
        System.out.println("Hello \tTacoma");

        System.out.println();
        System.out.println();

       /* hello tacoma inside back slashes
       the back slash escape sequence */
       System.out.println("\\Hello Tacoma\\");
    }
}
